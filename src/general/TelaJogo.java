    private void relancarDadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_relancarDadosActionPerformed
        if (limiteVez != 0) {
            Dado dad = new Dado();

            if (dado1.isSelected()) {
                dado1.setText(Integer.toString(dad.darValor()));
                dado1.setSelected(false);
            }
            if (dado2.isSelected()) {
                dado2.setText(Integer.toString(dad.darValor()));
                dado2.setSelected(false);
            }
            if (dado3.isSelected()) {
                dado3.setText(Integer.toString(dad.darValor()));
                dado3.setSelected(false);
            }
            if (dado4.isSelected()) {
                dado4.setText(Integer.toString(dad.darValor()));
                dado4.setSelected(false);
            }
            if (dado5.isSelected()) {
                dado5.setText(Integer.toString(dad.darValor()));
                dado5.setSelected(false);
            }
            limiteVez = limiteVez - 1;
        }
        if (limiteVez == 0) {
            relancarDados.setEnabled(false);
        }

    }//GEN-LAST:event_relancarDadosActionPerformed

