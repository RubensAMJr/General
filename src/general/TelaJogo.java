    private void resetarRodada() {
        limiteVez = 3;
        aremessarDados.setEnabled(true);
        relancarDados.setEnabled(false);
        btnFinaliza.setEnabled(false);
        vezJogador.setText("Vez do jogador: " + jogo.controladorMain());
        rodada = rodada - 1;
        lblRodada.setText("Rodada: " + rodada);
        resetarDados();

    }

    private int selecionarColuna(JTable table, String name) {
        for (int i = 0; i < table.getColumnCount(); ++i) {
            if (table.getColumnName(i).equals(name)) {
                return i;
            }
        }
        return -1;
    }

    private Integer verificarJogador(int pt) {
        if (jogo.getVez() == 1) {
            total1 = total1 + pt;
            return total1;
        }
        if (jogo.getVez() == 2) {
            total2 = total2 + pt;
            return total2;
        }
        if (jogo.getVez() == 3) {
            total3 = total3 + pt;
            return total3;
        }
        if (jogo.getVez() == 4) {
            total4 = total4 + pt;
            return total4;
        }
        return 0;
    }

    private void tratarJogadaP(int j, int i0, ArrayList<Integer> dados) {

        int pt = 0;
        for (int i = 0; i < dados.size(); i++) {
            if (dados.get(i) == j) {
                pt += (1 * j);
            }
        }
        if (pt == 0) {
            JOptionPane.showMessageDialog(null, "Jogada invalida! ");
        } else {

            tabelaPontos.setValueAt(pt + (Integer) (tabelaPontos.getValueAt(0, jogo.getVez())), i0, jogo.getVez());
            tabelaPontos.setValueAt(verificarJogador(pt), 13, jogo.getVez());
            resetarRodada();
        }

    }


